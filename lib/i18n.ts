export function useTranslation() {
  const language = useHaloboardStore((state) => state.language)
  const t = (key: string, category?: string) => {
    const langData = translations[language] || translations.en
    if (category && (langData as any)[category]) {
      return (langData as any)[category][key] || key
    }
    return (langData as any)[key] || key
  }
  return { t, language }
}